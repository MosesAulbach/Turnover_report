# Performancekennzahlen:

# Teile pro Rechnung
def calc_upt(art,trans):
    upt = art / trans
    return upt

# Durchschnittl. Wert pro Rechnung (1 Rechnung == 1 Transaktion)
def calc_atv(rev, trans):
    atv = rev / trans
    return atv

# Durschnittl. Wert pro Artikel
def calc_aur(rev, art):
    aur = rev / art
    return aur

# Wert pro Besucher
def calc_sot(rev, visit):
    sot = rev / visit
    return sot









